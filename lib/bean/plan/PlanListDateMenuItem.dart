class PlanListDateMenuItem {
  bool checkStatus;
  int day;
  String week;

  bool getCheckStatus() {
    return checkStatus;
  }

  void setCheckStatus(bool status) {
    this.checkStatus = status;
  }

  int getDay() {
    return day;
  }

  void setDay(int day) {
    this.day = day;
  }

  String getWeek() {
    return week;
  }

  void setWeek(String week) {
    this.week = week;
  }
}